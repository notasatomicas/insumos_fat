        </li>
        <li>
          <strong>Información de Uso:</strong> Recopilamos información sobre
          cómo interactúas con nuestro sitio web o nuestra aplicación, incluidos
          los libros que visualizas, añades al carrito o compras, así como tu
          actividad de navegación y patrones de uso.
        </li>
        <li>
          <strong>Información de Dispositivos:</strong> Podemos recopilar
          información sobre el dispositivo que utilizas para acceder a nuestros
          servicios, como tu dirección IP, tipo de navegador, sistema operativo
          y identificadores únicos de dispositivos.
        </li>
      </ul>

      <h2>2. Uso de la Información:</h2>
      <p class="lead">
        Utilizamos la información que recopilamos para procesar tus pedidos,
        proporcionarte servicios personalizados, mejorar la funcionalidad de
        nuestro sitio web y aplicación, y comunicarnos contigo sobre tu cuenta y
        tus compras. También podemos utilizar tu información para enviarte
        correos electrónicos promocionales, boletines informativos u otra
        información relevante sobre nuestros productos y servicios, siempre y
        cuando hayas dado tu consentimiento para recibir dichas comunicaciones.
      </p>

      <h2>3. Compartir Información:</h2>
      <p class="lead">
        No vendemos, alquilamos ni compartimos tu información personal con
        terceros con fines comerciales sin tu consentimiento expreso, excepto
        según lo requiera la ley o en cumplimiento de una orden judicial.
        Podemos compartir tu información con proveedores de servicios de
        confianza que nos ayudan a operar nuestro negocio y prestar servicios a
        nuestros clientes, como procesadores de pagos, servicios de envío y
        proveedores de alojamiento web.
      </p>

      <h2>4. Seguridad de la Información:</h2>
      <p class="lead">
        Implementamos medidas de seguridad técnicas, administrativas y físicas
        para proteger tu información contra accesos no autorizados, divulgación,
        alteración o destrucción. Sin embargo, debes tener en cuenta que ninguna
        medida de seguridad es absolutamente infalible, y no podemos garantizar
        la seguridad absoluta de tu información en todo momento.
      </p>

